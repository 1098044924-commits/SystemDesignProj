package org.example.accounting.domain;

/**
 * 账户类型枚举
 * 对应会计科目五大类：资产、负债、权益、收入、费用。
 */
public enum AccountType {

    /**
     * 资产类
     */
    ASSET,

    /**
     * 负债类
     */
    LIABILITY,

    /**
     * 所有者权益类
     */
    EQUITY,

    /**
     * 收入类
     */
    INCOME,

    /**
     * 费用/支出类
     */
    EXPENSE
}












